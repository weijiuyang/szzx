def current_release_notes() -> str:
    return CHANGELOG[0]["notes"]


def changelog_text(limit: int | None = None) -> str:
    entries = CHANGELOG if limit is None else CHANGELOG[:limit]
    lines: list[str] = []
    for entry in entries:
        date = f" · {entry['date']}" if entry["date"] else ""
        lines.append(f"v{entry['version']}{date}\n{entry['notes']}")
    return "\n\n".join(lines)
